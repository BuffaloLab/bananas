namespace Facebook.Unity
{
    /// <summary>
    /// Http method.
    /// </summary>
    public enum HttpMethod
    {
        /// <summary>
        /// Indicates that the request is a GET request.
        /// </summary>
        GET,
        /// <summary>
        /// Indicates that the request is a POST request.
        /// </summary>
        POST,
        /// <summary>
        /// Indicates that the request is a DELETE request.
        /// </summary>
        DELETE,
    }
}
